export async function GetResourceInfo(client, ResourceName) {
  const ResourceInfo = {
    code: null,
    message: null,
    Name: null,
    ID: null,
    DeveloperName: null,
    Size: null,
    Star: null,
    DownloadCount: null,
    Point: null,
    Diamond: null,
    Image: null,
    OnlyVip: null,
  };
  try {
    const Response = await client.doRequest(
      client.x19Session.releaseInfoPE.WebServerUrl,
      "/pe-item/query/search-by-keyword/",
      {
        body: {
          offset: 0,
          length: 20,
          keyword: ResourceName,
          init: 0,
          first_type: 0,
          channel_id: 5,
        },
      }
    );
    ResourceInfo.code = Response.code;
    ResourceInfo.message = Response.message;
    ResourceInfo.Name = Response.entities[0].res_name;
    ResourceInfo.ID = Response.entities[0].item_id;
    ResourceInfo.DeveloperName = Response.entities[0].developer_name;
    ResourceInfo.Size = Response.res_size;
    ResourceInfo.Star = Response.stars;
    ResourceInfo.DownloadCount = Response.entities[0].download_num;
    ResourceInfo.Point = Response.entities[0].points;
    ResourceInfo.Diamond = Response.entities[0].diamond;
    ResourceInfo.Image = Response.entities[0].title_image_url;
    ResourceInfo.OnlyVip = Response.entities[0].vip_only;
  } catch (error) {
    if (error.errorCode === "HTTP_REQUEST_FAILED") {
      if (error.response.code === 10 && error.response.message === "请先登录") {
        ResourceInfo.code = error.response.code;
        ResourceInfo.message = error.response.message;
      }
    } else if (error.errorCode === "NETWORK_ERROR") {
      ResourceInfo.code = 100001;
      ResourceInfo.message = "网络异常";
    } else {
      console.error(error);
      throw error;
    }
  }
  return ResourceInfo;
}

